#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*t1;
	unsigned char	*t2;

	i = 0;
	t1 = (unsigned char *)s1;
	t2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	while ((t1[i] || t2[i]) && (t1[i] == t2[i]) && (i < n - 1))
		i++;
	return (t1[i] - t2[i]);
}
