#include "libft.h"
#include <stdio.h>

void	ft_bzero(void *str, size_t len)
{
	char	*s;
	size_t	i;

	i = 0;
	s = str;
	while (len > 0)
	{
		s[i] = 0;
		len--;
		i++;
	}
}
