#include "libft.h"

int	ft_atoi(const char *str)
{
	int	a;
	int	neg;
	int	c;

	a = 0;
	c = 0;
	neg = 1;
	while ((str[a] >= 9 && str[a] <= 13) || (str[a] == ' '))
		a++;
	if (str[a] == '-' || str[a] == '+')
	{
		if (str[a] == '-')
			neg = -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		c = c * 10 + (str[a] - '0');
		a++;
	}
	return (c * neg);
}
